import os
import sys
import click
from click import confirmation_option

from flask import Flask, render_template, request, redirect, url_for, flash
from flask_sqlalchemy import SQLAlchemy
from werkzeug.security import generate_password_hash, check_password_hash

WIN = sys.platform.startswith('win')
if WIN:  # 如果是 Windows 系统，使用三个斜线
    prefix = 'sqlite:///'
else:  # 否则使用四个斜线
    prefix = 'sqlite:////'

app = Flask(__name__)
app.config['SQLALCHEMY_DATABASE_URI'] = prefix + os.path.join(app.root_path, 'data.db')
app.config['SQLALCHEMY_TRACK_MODIFICATIONS'] = False  # 关闭对模型修改的监控
app.config['SECRET_KEY'] = 'dev'
# 在扩展类实例化前加载配置

db = SQLAlchemy(app)


class User(db.Model):  # 表名将会是 user（自动生成，小写处理）
    id = db.Column(db.Integer, primary_key=True)  # 主键
    name = db.Column(db.String(20))  # 名字
    username = db.Column(db.String(20))  # 登录用户名
    password_hash = db.Column(db.String(60))  # 密码散列值

    def set_password(self, password):
        self.password_hash = generate_password_hash(password)  # 对密码进行哈希

    def check_password(self, password):
        return check_password_hash(self.password_hash, password)  # 对比哈希的密码值


class Movie(db.Model):  # 表名将会是 movie
    id = db.Column(db.Integer, primary_key=True)  # 主键
    title = db.Column(db.String(60))  # 电影标题
    year = db.Column(db.String(4))  # 电影年份


@app.cli.command()
@click.option('--drop', is_flag=True, help='Create after drop.')  # 在使用命令flask initdb 时可以选择使用或不使用--drop，
# 作用和action='store_true'类似
def initdb(drop):
    '''Initialize the database'''
    if drop:
        db.drop_all()
    db.create_all()
    click.echo('Initialized database.')


